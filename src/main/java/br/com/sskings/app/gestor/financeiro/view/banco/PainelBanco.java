    private void lblExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblExcluirMouseClicked
        int resp = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o Banco?");
        if(resp == 0){
            try{
                new BancoDao().excluir(banco);
                JOptionPane.showMessageDialog(this, "Banco excluÃ­do!");
            } catch(Exception ex){
                JOptionPane.showMessageDialog(this, "Erro ao tentar excluir um Banco");
            }
        }
    }//GEN-LAST:event_lblExcluirMouseClicked

    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked
        if(!txtNome.getText().isBlank() && !txtCodigo.getText().isBlank()) {            
            try{
                
                String nome = txtNome.getText();
                Integer codigo = Integer.valueOf(txtCodigo.getText());
                banco.setNome(nome);
                banco.setCodigo(codigo);
                new BancoDao().atualizar(banco);
                JOptionPane.showMessageDialog(this, "Os dados do banco foram atualizado.");
                                
                txtCodigo.setEditable(false);
                btnSalvar.setVisible(false);
                this.revalidate();
                this.repaint();
                
            }catch(Exception ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "NÃ£o foi possivel atualizar os dados do banco.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.");
        }
    }//GEN-LAST:event_btnSalvarMouseClicked


