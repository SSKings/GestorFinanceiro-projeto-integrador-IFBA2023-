    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        if (!txtNumero.getText().isBlank() && !txtLimite.getText().isBlank()
                && !txtMesV.getText().isBlank() && !txtAnoV.getText().isBlank()) {

            try {

                Long numero = Long.valueOf(txtNumero.getText());
                Double limite = Double.valueOf(txtLimite.getText());
                Integer mesVencimento = Integer.valueOf(txtMesV.getText());
                Integer anoVencimento = Integer.valueOf(txtAnoV.getText());

                var usuarioLogado = new UsuarioDto();
                usuarioLogado.setLogin(TelaLogin.getCampoUsuario());
                usuarioLogado = new UsuarioDao().pesquisar(usuarioLogado);
                
                
                CartaoCreditoDto novoCartao = new CartaoCreditoDto();
                novoCartao.setNumero(numero);
                novoCartao.setUsuario(usuarioLogado);
                novoCartao.setVencimento(LocalDate.of(anoVencimento, mesVencimento, 1));
                novoCartao.setLimite(limite);
                novoCartao.setLimite_disponivel(limite);
                
                BancoDto banco = new BancoDto();
                banco.setId(getIdBancoSelecionado());
                novoCartao.setBanco(banco);

                new CartaoCreditoDao().cadastrar(novoCartao);
                JOptionPane.showMessageDialog(this, "Cart√£o cadastrado com sucesso!");
                txtNumero.setText("");
                txtLimite.setText("");
                txtMesV.setText("");
                txtAnoV.setText("");

            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Erro ao tentar cadastrar um cart√£o");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed
    
    private void preencherComboBox(){
        try{
            List<BancoDto> bancos = new BancoDao().listar();
            comboBancos.removeAllItems();
            
            bancos.forEach(banco -> {
                
                comboBancos.addItem(banco.getNome());
                this.mapaBancoId.put(banco.getNome(), banco.getId());
                
            });
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, "N√£o foi poss√≠vel preencher os bancos.");
        }
        
    }
    
    public int getIdBancoSelecionado() {
        String itemSelecionado = comboBancos.getSelectedItem().toString();
        return this.mapaBancoId.get(itemSelecionado);
    }

