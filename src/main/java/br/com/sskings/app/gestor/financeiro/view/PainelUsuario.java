    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (!txtEmail.getText().isBlank() && !txtLogin.getText().isBlank()) {
            try {

                String email = txtEmail.getText();
                String login = txtLogin.getText();

                UsuarioDto usuario = obterUsuarioLogado();
                usuario.setEmail(email);
                usuario.setLogin(login);

                new UsuarioDao().alterar(usuario);
                JOptionPane.showMessageDialog(this, "Dados do usu√°rio alterados com sucesso.");
                txtEmail.setEditable(false);
                txtLogin.setEditable(false);
                btnSalvar.setVisible(false);
            } catch (Exception ex) {

            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        JOptionPane.showMessageDialog(this, "Ao excluir o usu√°rio, voc√™ perder√° o acesso ao MSM finan√ßas.");
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja excluir este usu√°rio?",
                "Confirmar exclus√£o", JOptionPane.YES_NO_OPTION);
        
        if (resposta == JOptionPane.YES_OPTION) {
            try {
                var usuario = obterUsuarioLogado();
                new UsuarioDao().excluir(usuario);
                new AgenciaDao().excluirTudo();
                JOptionPane.showMessageDialog(this, "Usu√°rio exclu√≠do com sucesso. At√© a pr√≥xima!");
                System.exit(0);
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }


    }//GEN-LAST:event_btnExcluirActionPerformed

