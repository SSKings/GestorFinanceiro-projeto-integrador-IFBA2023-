    private boolean enviarTokenParaEmail() {

        final String emailRemetente = "teste@hotmail.com";
        final String psw = "";

        Properties props = new Properties();
        props.put("mail.smtp.host", "smtp.office365.com");
        props.put("mail.smtp.port", "587");
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.starttls.enable", "true");

        Authenticator auth = new Authenticator() {
            @Override
            protected PasswordAuthentication getPasswordAuthentication() {
                return new PasswordAuthentication(emailRemetente, psw);
            }
        };

        Session session = Session.getInstance(props, auth);

        try {

            Message message = new MimeMessage(session);
            message.setFrom(new InternetAddress(emailRemetente));
            emailDestinatario = txtEmail.getText();
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(emailDestinatario));
            message.setSubject("Recuperação de Senha");
            token = geradorDeToken();
            String conteudoHTML = "<html><head><meta charset='UTF-8'></head><body style='background-color: gray; color: white;'>"
                    + "<h1>Recuperação de Senha</h1>"
                    + "<h3>Seu token de recuperação de senha é: <strong>" + token + "</strong></h3>"
                    + "</body></html>";
            message.setContent(conteudoHTML, "text/html; charset=UTF-8");

            Transport.send(message);
            JOptionPane.showMessageDialog(null, "E-mail de recuperação de senha enviado para " + emailDestinatario);
            return true;
        } catch (MessagingException e) {
            JOptionPane.showMessageDialog(null, "Não foi possível enviar token de recuperação.");
            return false;
        }

    }

    private String geradorDeToken() {

        SecureRandom geradorSeguro = new SecureRandom();
        byte[] bytesDoToken = new byte[10];
        geradorSeguro.nextBytes(bytesDoToken);
        String token = Base64.getUrlEncoder().withoutPadding().encodeToString(bytesDoToken);

        return token;

    }

