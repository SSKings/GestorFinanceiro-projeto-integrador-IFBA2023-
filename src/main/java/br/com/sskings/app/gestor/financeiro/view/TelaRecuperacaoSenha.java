    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        if (!txtEmail.getText().isBlank()) {
            if (enviarTokenParaEmail()) {

                PainelSenhaNova painel = new PainelSenhaNova(this);
                painel.setSize(410, 500);
                painel.setLocation(0, 0);

                painelRenderizador.remove(painelRecuperacao);
                painelRenderizador.add(painel);
                painelRenderizador.revalidate();
                painelRenderizador.repaint();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Campo de email vazio, informe o email de recuperação de senha.");
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

